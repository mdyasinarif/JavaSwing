    private void btnSaveCA1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveCA1ActionPerformed
        String AccountNo = shAccountNo.getText();
        String FromNO = shFromNO.getText();
        String CardNo = shCardNo.getText();
        String Name = shName.getText();
        String MotherName = shMotherName.getText();
        String FatherHusbendName = shFatherHusbendName.getText();
        String PresentAddress = shPresentAddress.getText() + " " + shPresentAddressPO.getText() + " " + shPresentAddressPS.getText() + " " + shPresentAddressD.getText();
        String ParmanetAddress = shParmanetAddress.getText() + " " + shParmanetAddressPO.getText() + " " + shParmanetAddressPS.getText() + " " + shParmanetAddressD.getText();
        String DateofBirth = shDateofBirth.getText();
        String AdmissionDate = shAdmissionDate.getText();
        String NIDNo = shNID.getText();
        String MobileNo = shMobilNo.getText();
        
        String ShareAmount = shShareAmount.getText();
        String NoOfShare = shNoOfShare.getText();
        String NomineName = shNomineName.getText();
        String Relations = shRelations.getText();
        String Share = shShare.getText();
        Connection con = null;
        PreparedStatement pst = null;
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/dbssbbss", "root", "1234");
            pst = con.prepareStatement("insert into shareaccountinfo values(?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?)");
            pst.setString(1, AccountNo);
            pst.setString(2, FromNO);
            pst.setString(3, CardNo);
            pst.setString(4, Name);
            pst.setString(5, MotherName);
            pst.setString(6, FatherHusbendName);
            pst.setString(7, PresentAddress);
            pst.setString(8, ParmanetAddress);
            pst.setString(9, DateofBirth);
            pst.setString(10, AdmissionDate);
            pst.setString(11, NIDNo);
            pst.setString(12, MobileNo);
            pst.setString(13, ShareAmount);
            pst.setString(14, NoOfShare);
            pst.setString(15, NomineName);
            pst.setString(16, Relations);
            pst.setString(17, Share);
            int i = pst.executeUpdate();
            if (i > 0) {
                JOptionPane.showMessageDialog(null, "Data is Saved");
            } else {
                JOptionPane.showMessageDialog(null, "Data is Not Saved");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_btnSaveCA1ActionPerformed

