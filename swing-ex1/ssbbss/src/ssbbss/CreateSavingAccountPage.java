    private void btnSaveCA1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveCA1ActionPerformed
       String AccountNo = sAccountNo.getText();
       String FromNO = sFromNO.getText();
       String CardNo = sCardNo.getText();
       String Name = sName.getText();
       String MotherName = sMotherName.getText();
       String FatherHusbendName = sFatherHusbendName.getText();
       String PresentAddress = sPresentAddress.getText()+" "+sPresentAddressPO.getText()+" "+sPresentAddressPS.getText()+" "+sPresentAddressD.getText();
       String ParmanetAddress = sParmanetAddress.getText()+" "+sParmanetAddressPO.getText()+" "+sParmanetAddressPS.getText()+" "+sParmanetAddressD.getText();
       String DateofBirth = sDateofBirth.getText();
       String AdmissionDate = sAdmissionDate.getText();
       String NIDNo = sNID.getText();
       String MobileNo = sMobilNo.getText();
       String SavingType = sSavingType.getSelectedItem().toString();
       String SavingsAmount = sSavingsAmount.getText();
       String NomineName = sNomineName.getText();
       String Relations = sRelations.getText();
       String Share = sShare.getText();
       Connection con = null;
       PreparedStatement pst = null;
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/dbssbbss", "root", "1234");
            pst = con.prepareStatement("insert into savingaccountinfo values(?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?)");
            pst.setString(1, AccountNo);
            pst.setString(2, FromNO);
            pst.setString(3, CardNo);
            pst.setString(4, Name);
            pst.setString(5, MotherName);
            pst.setString(6, FatherHusbendName);
            pst.setString(7, PresentAddress);
            pst.setString(8, ParmanetAddress);
            pst.setString(9, DateofBirth);
            pst.setString(10, AdmissionDate);
            pst.setString(11, NIDNo);
            pst.setString(12, MobileNo);
            pst.setString(13, SavingType);
            pst.setString(14, SavingsAmount);
            pst.setString(15, NomineName);
            pst.setString(16, Relations);
            pst.setString(17, Share);
            int i = pst.executeUpdate();
            if (i>0) {
                JOptionPane.showMessageDialog(null, "Data is Saved");
            } else {
                JOptionPane.showMessageDialog(null, "Data is Not Saved");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_btnSaveCA1ActionPerformed

