       public static boolean emailValid(String email){
           int adpos = email.indexOf("@");
           int dotpos = email.indexOf(".");
           if (adpos>1&&(dotpos-adpos)>2&&dotpos<email.length()-2) {
               return true;
           }
           return false;
       }
    private void btnaddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaddActionPerformed
       String id="0",name="",age="0",email="",gender="",course="",round="",pasword="",confirmPass="",comment="";
        if (Integer.parseInt(txtID.getText())<=2) {
            JOptionPane.showMessageDialog(null, "ID at least 2 character");
        } 
        else if (txtName.getText().length()<=3) {
            JOptionPane.showMessageDialog(null, "Name at least 3 character");
        }
        else if (Integer.parseInt(txtAge.getText())<=17 || Integer.parseInt(txtAge.getText())>=70) {
            
            
            JOptionPane.showMessageDialog(null, "Age Between 18 to 70");
        }
        else if (!emailValid(txtEmail.getText())) {
            JOptionPane.showMessageDialog(null, "Invalid Email");
        }
        else if (!rMale.isSelected() && !rFeMale.isSelected()) {
            JOptionPane.showMessageDialog(null, "Select Your Gender");
        }
        else if (!cheHRML.isSelected() && !cheJava.isSelected()) {
            JOptionPane.showMessageDialog(null, "Select Your Courses");
        }
        else if (comRound.getItemAt(comRound.getSelectedIndex()) == "Select A Round") {
            JOptionPane.showMessageDialog(null, "Select Your Round");
        }
//        else if (txtPassword.getText().length()<=3) {
//            JOptionPane.showMessageDialog(null, "Password Must have 3 character");
//        }
//        else if ((txtPassword.getText().trim())!= (txtConfirm.getText().trim() ) ) {
//            JOptionPane.showMessageDialog(null, "Password Do Not Match");
//        }
        else if (txtComment.getText().length()<=10 ){
            JOptionPane.showMessageDialog(null, "Comment must have 10 character");
        }
        else {
            id =txtID.getText();
            name=txtName.getText();
            age=txtAge.getText();
            email=txtEmail.getText();
            gender = rMale.isSelected()? "Male": "Female";
            course += cheHRML.getText();
            course += cheJava.getText();
            round = comRound.getItemAt(comRound.getSelectedIndex());
            
            comment = txtComment.getText();
            
            DefaultTableModel model = (DefaultTableModel) tblDisplay.getModel();
            Object[] col = new Object[8];
            col[0] = id;
            col[1] = name;
            col[2] = age;
            col[3] = email;
            col[4] = gender;
            col[5] = course;
            col[6] = round;
            col[7] = comment;
            model.addRow(col);
            
            Student student = new Student(Integer.parseInt(id), name, Integer.parseInt(age), email, gender, course, round, comment);
            List<Student>students = new ArrayList<>();
            students.add(student);
            
            Utiles.writeToFile(JOptionPane.showInputDialog("Enter File Name"), students);
        }
    }//GEN-LAST:event_btnaddActionPerformed

