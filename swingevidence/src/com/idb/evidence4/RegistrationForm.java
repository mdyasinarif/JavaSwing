    private void jTextField_ageKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField_ageKeyReleased
        // TODO add your handling code here:
        String stringAge = jTextField_age.getText();
        if (!stringAge.equals("")) {
            try {
                int age = Integer.parseInt(stringAge);

                if (age > 24 && age < 70) {
                    isValidAge = true;
                    lbl_age.setText("");

                } else {
                    lbl_age.setText("Enter valid age");
                }

            } catch (NumberFormatException e) {
                lbl_age.setText("Enter valid age");
            }
        }

    }//GEN-LAST:event_jTextField_ageKeyReleased

    private void jTextField_emailKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField_emailKeyReleased
        // TODO add your handling code here:
        
        String email = jTextField_email.getText().trim();
        Pattern pat = Pattern.compile("[a-zA-Z0-9][a-zA-Z0-9_.]*[@][a-zA-Z0-9]+([.][a-zA-Z]+)+");
        Matcher match = pat.matcher(email);
        
        if (match.find()) {
            isValidEmail = true;
            lbl_eamil.setText("");

        } else {
            lbl_eamil.setText("Enter valid email");
        }

    }//GEN-LAST:event_jTextField_emailKeyReleased

