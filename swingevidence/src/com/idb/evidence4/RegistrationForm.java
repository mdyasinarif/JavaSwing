    void displayDataInTable(int id, String name, int age, String email, String gender, String cc, String round, String comment) {
        DefaultTableModel tblModel = (DefaultTableModel) data_table.getModel();

        Object[] row = new Object[8];
        row[0] = id;
        row[1] = name;
        row[2] = age;
        row[3] = email;
        row[4] = gender;
        row[5] = cc;
        row[6] = round;
        row[7] = comment;
        tblModel.addRow(row);
    }

