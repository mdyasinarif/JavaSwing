    private void btn_read_from_fileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_read_from_fileActionPerformed
        DefaultTableModel tblModel = (DefaultTableModel) data_table.getModel();
        tblModel.setRowCount(0);

        try {
            File f = new File("data.txt");
            Scanner inputFile = new Scanner(f);
            while (inputFile.hasNext()) {
                int id = Integer.parseInt(inputFile.next());
                String name = inputFile.next();
                int age = Integer.parseInt(inputFile.next());
                String email = inputFile.next();
                String gender = inputFile.next();
                String cc = inputFile.next();
                String round = inputFile.next();
                String comment = inputFile.next();

                displayDataInTable(id, name, age, email, gender, cc, round, comment);
                // System.out.println(inputFile.next());
            }
        } catch (FileNotFoundException ex) {
            Logger.getLogger(RegistrationForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btn_read_from_fileActionPerformed

