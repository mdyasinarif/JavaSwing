    public void displayRoleListIntoTable() {
        clearTable();
        RoleDao role = new RoleDaoImp();
        List<Role> list = role.getRoles();

        DefaultTableModel model = (DefaultTableModel) tblRoleList.getModel();

        Object[] col = new Object[2];
        for (int i = 0; i < list.size(); i++) {
            col[0] = list.get(i).getId();
            col[1] = list.get(i).getRoleName();
            model.addRow(col);
        }

    }
    public void clearTable(){
        DefaultTableModel model = (DefaultTableModel) tblRoleList.getModel();
        model.setRowCount(0);
        
    }
    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        RoleDao obj = new RoleDaoImp();
        String roleName = txtRoleName.getText().trim();
        Role role = new Role(roleName);
        obj.save(role);
        displayRoleListIntoTable();
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        RoleDao obj = new RoleDaoImp();
        String roleName = txtRoleName.getText().trim();
        Role role = new Role(roleID, roleName);
        obj.update(role);
        displayRoleListIntoTable();
        
    }//GEN-LAST:event_btnEditActionPerformed
    int roleID=0;
    private void tblRoleListMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblRoleListMouseClicked
        int i = tblRoleList.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel) tblRoleList.getModel();
        roleID = Integer.parseInt(model.getValueAt(i, 0).toString());
        txtRoleName.setText(model.getValueAt(i, 1).toString());
        
    }//GEN-LAST:event_tblRoleListMouseClicked

